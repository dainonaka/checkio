def disconnected_users(net, users, source, crushes):
    connected = {source}
    while True:
        for link in net:
            if len(set(link) & connected) == 1 and \
               len(set(link) & set(crushes)) == 0:
                connected = connected | set(link)
                break
        else: break
    return sum([users[node] for node in set(users.keys()) - connected])
        

